  <h2>Right to Information (RTI) Act, 2005</h2>
  <p>The Right to Information Act, 2005, is a landmark legislation enacted by the Government of India to promote transparency and accountability in the working of public authorities. It empowers citizens to request information from public authorities, thereby fostering an open and accountable government.</p>

  <h3>Key Features</h3>
  <ul>
    <li><strong>Access to Information:</strong> Citizens can request information from any public authority, which is required to reply expeditiously or within thirty days.</li>
    <li><strong>Public Authorities:</strong> The Act applies to all constitutional authorities, including executive, legislature, and judiciary; any institution or body established or constituted by an Act of Parliament or a state legislature.</li>
    <li><strong>Information Commissions:</strong> The Act provides for the constitution of a Central Information Commission and State Information Commissions to oversee the implementation of the Act.</li>
    <li><strong>Exemptions:</strong> Certain organizations, such as intelligence and security agencies, are exempted from the RTI Act except in cases of corruption or human rights violations.</li>
  </ul>

  <h3>How to File an RTI</h3>
  <ol>
    <li>Identify the public authority you wish to seek information from.</li>
    <li>Draft a clear and concise application specifying the information sought.</li>
    <li>Submit the application to the designated Public Information Officer (PIO) of the concerned public authority.</li>
    <li>Pay the prescribed fee, if applicable.</li>
    <li>If not satisfied with the response, you can file an appeal with the First Appellate Authority.</li>
  </ol>

